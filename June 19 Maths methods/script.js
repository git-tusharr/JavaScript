 // Rounding Methods
    let num1 = 4.6;
    let num2 = 4.4;
    console.log("Math.round(4.6) →", Math.round(num1));  // → 5
    console.log("Math.round(4.4) →", Math.round(num2));  // → 4

    console.log("Math.floor(4.6) →", Math.floor(num1));  // → 4
    console.log("Math.ceil(4.4) →", Math.ceil(num2));    // → 5
    console.log("Math.trunc(4.9) →", Math.trunc(4.9));   // → 4

    // Square Root
    console.log("Math.sqrt(25) →", Math.sqrt(25));       // → 5
    console.log("Math.sqrt(2) →", Math.sqrt(2));         // → ~1.41

    // Power
    console.log("Math.pow(2, 3) →", Math.pow(2, 3));     // → 8
    console.log("Math.pow(5, 2) →", Math.pow(5, 2));     // → 25

    // Min and Max
    console.log("Math.min(4, 2, 7, 1) →", Math.min(4, 2, 7, 1));  // → 1
    console.log("Math.max(4, 2, 7, 1) →", Math.max(4, 2, 7, 1));  // → 7